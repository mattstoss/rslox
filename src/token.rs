#[derive(Debug, PartialEq, Clone)]
pub enum TokenKind {
    And,
    Bang,
    BangEqual,
    Class,
    Comma,
    Dot,
    Else,
    EndOfFile,
    Equal,
    EqualEqual,
    False,
    For,
    Fun,
    Greater,
    GreaterEqual,
    If,
    LeftBrace,
    LeftParen,
    Less,
    LessEqual,
    Minus,
    Nil,
    Number(i32),
    Or,
    Plus,
    Print,
    Return,
    RightBrace,
    RightParen,
    Semicolon,
    Slash,
    Star,
    String(String),
    Super,
    This,
    True,
    Var,
    While,
}

#[derive(Debug)]
pub struct Token {
    pub kind: TokenKind,
}
